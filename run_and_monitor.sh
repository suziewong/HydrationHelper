#!/bin/bash

echo "=========================================="
echo "喝水提醒 - 启动并监控日志"
echo "=========================================="
echo ""

# 停止现有实例
echo "🛑 停止现有实例..."
killall "喝水提醒" 2>/dev/null
sleep 1

echo ""
echo "🚀 启动应用..."
echo "------------------------------------------"

# 在后台运行应用并捕获所有输出
/Applications/喝水提醒.app/Contents/MacOS/喝水提醒 2>&1 | while read line; do
    echo "[$(date '+%H:%M:%S')] $line"
done &

APP_PID=$!

sleep 3

echo ""
echo "------------------------------------------"
echo "📊 应用状态检查"
echo "------------------------------------------"

# 检查进程
if ps -p $APP_PID > /dev/null 2>&1; then
    echo "✅ 应用正在运行 (PID: $APP_PID)"
else
    echo "❌ 应用进程已退出"
fi

# 检查 Dock
echo ""
echo "📱 检查 Dock 和菜单栏..."
echo "请查看："
echo "  1. 菜单栏右上角是否有 💧 图标"
echo "  2. 是否有弹出窗口"
echo "  3. 是否有通知权限请求"

echo ""
echo "=========================================="
echo "现在请："
echo "1. 点击菜单栏的 💧 图标"
echo "2. 点击 '测试通知' 按钮"
echo "3. 观察是否有权限请求弹窗"
echo ""
echo "如果弹窗出现，请点击'允许'"
echo "=========================================="
echo ""
echo "按 Ctrl+C 停止监控..."
echo "=========================================="

# 保持运行以监控日志
wait $APP_PID